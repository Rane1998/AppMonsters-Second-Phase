package com.example.appmonsters;

public class Review {

    private String titleAnswer;
    private String reviewAnswer;
    private String nicknameAnswer;
    private String emailAnswer;

    public String getTitleAnswer() {
        return titleAnswer;
    }

    public void setTitleAnswer(String titleAnswer) {
        this.titleAnswer = titleAnswer;
    }

    public String getReviewAnswer() {
        return reviewAnswer;
    }

    public void setReviewAnswer(String reviewAnswer) {
        this.reviewAnswer = reviewAnswer;
    }

    public String getNicknameAnswer() {
        return nicknameAnswer;
    }

    public void setNicknameAnswer(String nicknameAnswer) {
        this.nicknameAnswer = nicknameAnswer;
    }

    public String getEmailAnswer() {
        return emailAnswer;
    }

    public void setEmailAnswer(String emailAnswer) {
        this.emailAnswer = emailAnswer;
    }

    public Review() {
    }

}
